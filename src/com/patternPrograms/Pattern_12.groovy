package com.patternPrograms

for (int i = 0; i <= 5; i++){
     int alphabet = 65;
     for (int j = 0; j <= i; j++){
          print((char) (alphabet + j));
          }
          println();
        }
for (int i = 5; i >= 0; i--){
     int alphabet = 65;
     for (int j = 0; j <= i - 1; j++){
          print((char) (alphabet + j));
          }
          println();
        }
